<script>
    export default {
        data(){
          return {
              form: new form({
                  id:'',
                  name: '',
                  email: '',
                  password: '',
                  type: '',
                  bio: '',
                  photo: ''
              })
          }
        },
        mounted() {
            console.log('Component mounted.')
        },
        methods: {
            getUserType(){
                return this.form.type;
            },
            getUserName(){

                return this.form.name;
            },
            getUserPhoto(){
                return (this.form.photo.length > 100) ? this.form.photo :"img/profile/"+this.form.photo ;
                //"img/profile/"+this.form.photo;
            },
            updateProfile(){
                this.$Progress.start();
                this.form.put('api/profile')
                    .then(()=>{
                        this.$Progress.finish();
                        toast.fire({
                            icon: 'success',
                            title: 'The User Updated successfully'
                        })
                    })
                    .catch(()=>{
                        this.$Progress.fail();
                        toast.fire({
                            icon: 'warning',
                            title: 'The User Not Updated successfully'
                        })
                    });
            },
            uploadPhoto(e){
                let file = e.target.files[0];
                // console.log(file);
                let reader = new FileReader();
                if(file['size'] < 2111775) {
                    reader.onloadend = (file) => {
                        this.form.photo = reader.result;
                    }
                    reader.readAsDataURL(file);
                }else{
                    swal.fire({
                        title: 'Ooops...',
                        text: 'You Are Trying to upload a large file .... Please select a one less than 2MB',
                        icon: 'warning'
                    })
                }

            }
        },
        created() {
            axios.get("api/profile").then(({data})=> (this.form.fill(data)));
        }
    }
</script>
